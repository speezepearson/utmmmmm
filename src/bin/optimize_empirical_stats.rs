use std::collections::BTreeMap;

use utmmmmm::compiled::{CState, CSymbol, CompiledTuringMachineSpec};
use utmmmmm::infinity::InfiniteTape;
use utmmmmm::optimization_hints::make_my_utm_self_optimization_hints;
use utmmmmm::tm::{Dir, RunningTuringMachine, TuringMachineSpec};
use utmmmmm::utm::{make_utm_spec, MyUtmSpec, MyUtmSpecOptimizationHints, State, Symbol};

fn run_and_tally(
    encoder: &MyUtmSpecOptimizationHints<MyUtmSpec>,
    max_steps: u64,
) -> (u64, BTreeMap<(State, Symbol), usize>) {
    let compiled = CompiledTuringMachineSpec::compile(encoder.guest).expect("UTM should compile");

    let mut tm = RunningTuringMachine::new(&compiled);
    let background = InfiniteTape::new(encoder);

    let mut tallies: [usize; 256 * 256] = [0; 256 * 256];
    let mut inner_steps: u64 = 0;
    let mut prev_state = tm.state;

    for i in 0..max_steps {
        if tm.pos >= tm.tape.len() {
            background.extend_compiled(&mut tm.tape, tm.pos + 1, &compiled);
        }

        let sym = tm.tape[tm.pos];
        if let Some((ns, nsym, dir)) = compiled.get_transition(tm.state, sym) {
            // Tally the transition using original (decompiled) state/symbol
            tallies[(tm.state.0 as usize) * 256 + sym.0 as usize] += 1;

            tm.state = ns;
            tm.tape[tm.pos] = nsym;
            tm.pos = match dir {
                Dir::Left => tm.pos.saturating_sub(1),
                Dir::Right => tm.pos + 1,
            };

            // Detect inner step
            if compiled.is_tick_boundary(prev_state, tm.state) {
                inner_steps += 1;
            }
            prev_state = tm.state;

            if i % 1_000_000_000 == 0 {
                eprintln!("{} steps", i);
            }
        } else {
            break;
        }
    }

    (
        inner_steps,
        BTreeMap::from_iter(tallies.iter().enumerate().filter(|(_, &c)| c > 0).map(
            |(i, &count)| {
                (
                    (
                        compiled.decompile_state(CState((i / 256) as u8)),
                        compiled.decompile_symbol(CSymbol((i % 256) as u8)),
                    ),
                    count,
                )
            },
        )),
    )
}

fn codegen(stats: &BTreeMap<(State, Symbol), usize>) -> String {
    // Sort by tally count ascending (least used first, most used last)
    let mut entries: Vec<_> = stats.iter().collect();
    entries.sort_by_key(|(_, &count)| count);

    let mut code = String::new();
    code.push_str("// Auto-generated by optimize_hints. Do not edit manually.\n");
    code.push_str("// This file contains empirically-derived transition statistics\n");
    code.push_str("// for optimizing UTM rule ordering.\n\n");
    code.push_str("use std::collections::BTreeMap;\n\n");
    code.push_str("use crate::utm::{ State, Symbol, };\n\n");
    code.push_str(
        "pub fn make_empirical_transition_stats() -> BTreeMap<(State, Symbol), usize> {\n",
    );
    code.push_str("    BTreeMap::from([\n");
    for ((state, symbol), count) in &entries {
        code.push_str(&format!(
            "        ((State::{:?}, Symbol::{:?}), {}),\n",
            state, symbol, count
        ));
    }

    code.push_str("    ])\n");
    code.push_str("}\n");

    code
}

fn main() {
    let max_steps: u64 = std::env::args()
        .nth(1)
        .and_then(|s| s.parse().ok())
        .unwrap_or(1_000_000_000);

    let utm_spec = make_utm_spec();

    // Start with current best hints
    let initial_hints = make_my_utm_self_optimization_hints(&utm_spec);

    let mut best_inner_steps: u64 = 0;
    let mut best_stats: Option<BTreeMap<(State, Symbol), usize>> = None;
    let mut current_hints = initial_hints;
    let mut iteration = 0;
    let mut prev_inner_steps: u64 = 0;

    loop {
        iteration += 1;
        eprintln!("=== Iteration {} ===", iteration);

        let (inner_steps, stats) = run_and_tally(&current_hints, max_steps);
        eprintln!(
            "  {} inner steps in {} outer steps (ratio {:.1})",
            inner_steps,
            max_steps,
            if inner_steps > 0 {
                max_steps as f64 / inner_steps as f64
            } else {
                f64::INFINITY
            },
        );

        if inner_steps > best_inner_steps {
            best_inner_steps = inner_steps;
            best_stats = Some(stats.clone());
            eprintln!("  New best! {} inner steps", best_inner_steps);
        }

        // Check convergence: this iteration's count >= 0.99 * previous iteration's count
        if iteration > 3 && inner_steps as f64 <= 1.01 * prev_inner_steps as f64 {
            eprintln!(
                "  Converged ({} <= 1.01 * {} = {:.0}). Done.",
                inner_steps,
                prev_inner_steps,
                1.01 * prev_inner_steps as f64,
            );
            break;
        }

        prev_inner_steps = inner_steps;

        // Build new hints from the tallies
        current_hints = MyUtmSpecOptimizationHints::from_transition_stats(&utm_spec, &stats);
    }

    let best = best_stats.expect("should have at least one result");
    let code = codegen(&best);

    let out_path = std::path::Path::new("src/empirical_transition_stats.rs");
    std::fs::write(out_path, &code).expect("failed to write output file");
    eprintln!(
        "Wrote {} with {} inner steps (best)",
        out_path.display(),
        best_inner_steps
    );
}
