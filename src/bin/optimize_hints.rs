use std::collections::HashMap;

use utmmmmm::compiled::CompiledTuringMachineSpec;
use utmmmmm::infinity::InfiniteTape;
use utmmmmm::tm::{Dir, RunningTuringMachine, TuringMachineSpec};
use utmmmmm::utm::{make_utm_spec, MyUtmSpec, MyUtmSpecOptimizationHints, State, Symbol, TmTransitionStats};

fn run_and_tally(
    utm_spec: &MyUtmSpec,
    hints: &MyUtmSpecOptimizationHints<MyUtmSpec>,
    max_steps: u64,
) -> (u64, TmTransitionStats<MyUtmSpec>) {
    let compiled = CompiledTuringMachineSpec::compile(utm_spec).expect("UTM should compile");
    let mark_rule_cstate = compiled.compile_state(State::MarkRule);

    let mut tm = RunningTuringMachine::new(&compiled);
    let background = InfiniteTape::new(utm_spec, hints);

    let mut tallies: HashMap<(State, Symbol), usize> = HashMap::new();
    let mut inner_steps: u64 = 0;
    let mut prev_state = tm.state;

    for _ in 0..max_steps {
        if tm.pos >= tm.tape.len() {
            background.extend_compiled(&mut tm.tape, tm.pos + 1, &compiled);
        }

        let sym = tm.tape[tm.pos];
        if let Some((ns, nsym, dir)) = compiled.get_transition(tm.state, sym) {
            // Tally the transition using original (decompiled) state/symbol
            let orig_state = compiled.decompile_state(tm.state);
            let orig_sym = compiled.decompile_symbol(sym);
            *tallies.entry((orig_state, orig_sym)).or_insert(0) += 1;

            tm.state = ns;
            tm.tape[tm.pos] = nsym;
            tm.pos = match dir {
                Dir::Left => tm.pos.saturating_sub(1),
                Dir::Right => tm.pos + 1,
            };

            // Detect inner step: entering MarkRule from DoneSeekHome
            // (In new layout, DoneSeekHome → MarkRule directly, not via Init)
            if tm.state == mark_rule_cstate && prev_state != mark_rule_cstate {
                inner_steps += 1;
            }
            prev_state = tm.state;
        } else {
            break;
        }
    }

    (inner_steps, TmTransitionStats(tallies))
}

fn codegen(stats: &TmTransitionStats<MyUtmSpec>) -> String {
    // Sort by tally count ascending (least used first, most used last)
    let mut entries: Vec<_> = stats.0.iter().collect();
    entries.sort_by_key(|(_, &count)| count);

    let mut code = String::new();
    code.push_str("// Auto-generated by optimize_hints. Do not edit manually.\n");
    code.push_str("// This file contains empirically-derived transition statistics\n");
    code.push_str("// for optimizing UTM rule ordering.\n\n");
    code.push_str("use std::collections::HashMap;\n\n");
    code.push_str("use crate::utm::{make_utm_spec, MyUtmSpec, MyUtmSpecOptimizationHints, State, Symbol, TmTransitionStats};\n\n");
    code.push_str("pub fn make_my_utm_self_optimization_hints() -> MyUtmSpecOptimizationHints<MyUtmSpec> {\n");
    code.push_str("    let stats = make_empirical_transition_stats();\n");
    code.push_str("    stats.make_optimization_hints(&make_utm_spec())\n");
    code.push_str("}\n\n");
    code.push_str("pub fn make_empirical_transition_stats() -> TmTransitionStats<MyUtmSpec> {\n");
    code.push_str("    TmTransitionStats(HashMap::from([\n");

    for ((state, symbol), count) in &entries {
        code.push_str(&format!(
            "        ((State::{:?}, Symbol::{:?}), {}),\n",
            state,
            symbol,
            count
        ));
    }

    code.push_str("    ]))\n");
    code.push_str("}\n");

    code
}

fn main() {
    let max_steps: u64 = std::env::args()
        .nth(1)
        .and_then(|s| s.parse().ok())
        .unwrap_or(1_000_000_000);

    let utm_spec = make_utm_spec();

    // Start with current best hints
    let initial_hints = utmmmmm::optimization_hints::make_my_utm_self_optimization_hints();

    let mut best_inner_steps: u64 = 0;
    let mut best_stats: Option<TmTransitionStats<MyUtmSpec>> = None;
    let mut current_hints = initial_hints;
    let mut iteration = 0;
    let mut prev_inner_steps: u64 = 0;

    loop {
        iteration += 1;
        eprintln!("=== Iteration {} ===", iteration);

        let (inner_steps, stats) = run_and_tally(&utm_spec, &current_hints, max_steps);
        eprintln!(
            "  {} inner steps in {} outer steps (ratio {:.1})",
            inner_steps,
            max_steps,
            if inner_steps > 0 { max_steps as f64 / inner_steps as f64 } else { f64::INFINITY },
        );

        if inner_steps > best_inner_steps {
            best_inner_steps = inner_steps;
            best_stats = Some(TmTransitionStats(stats.0.clone()));
            eprintln!("  New best! {} inner steps", best_inner_steps);
        }

        // Check convergence: this iteration's count >= 0.99 * previous iteration's count
        if iteration > 1 && inner_steps as f64 <= 1.01 * prev_inner_steps as f64 {
            eprintln!(
                "  Converged ({} <= 1.01 * {} = {:.0}). Done.",
                inner_steps,
                prev_inner_steps,
                1.01 * prev_inner_steps as f64,
            );
            break;
        }

        prev_inner_steps = inner_steps;

        // Build new hints from the tallies
        current_hints = stats.make_optimization_hints(&utm_spec);
    }

    let best = best_stats.expect("should have at least one result");
    let code = codegen(&best);

    let out_path = std::path::Path::new("src/empirical_transition_stats.rs");
    std::fs::write(out_path, &code).expect("failed to write output file");
    eprintln!(
        "Wrote {} with {} inner steps (best)",
        out_path.display(),
        best_inner_steps
    );
}
